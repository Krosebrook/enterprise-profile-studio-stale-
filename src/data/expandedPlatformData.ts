// Export helper functions
export function getPlatformsByEcosystem(ecosystem: string): AIPlatform[] {
  return expandedPlatforms.filter((p) => p.ecosystem === ecosystem);
}

export function getTopPlatformsByCategory(category: string, limit = 5): AIPlatform[] {
  return expandedPlatforms
    .filter((p) => p.category === category)
    .slice(0, limit);
}

export function getTier1Platforms(): AIPlatform[] {
  return expandedPlatforms.filter((p) => p.priority === 'Tier 1');
}

export function searchPlatforms(query: string): AIPlatform[] {
  const lowerQuery = query.toLowerCase();
  return expandedPlatforms.filter((p) => 
    p.name.toLowerCase().includes(lowerQuery) ||
    p.specialties.some((s) => s.toLowerCase().includes(lowerQuery)) ||
    p.ecosystem.toLowerCase().includes(lowerQuery)
  );
}
