// Aggregate all questions
export function getQuestionsForType(assessmentType: AssessmentType): QuestionWithType[] {
  const allQuestions = [
    ...ORGANIZATIONAL_CULTURE_QUESTIONS,
    ...DATA_INFRASTRUCTURE_QUESTIONS,
    ...TECHNICAL_CAPABILITIES_QUESTIONS,
    ...TALENT_SKILLS_QUESTIONS,
    ...GOVERNANCE_COMPLIANCE_QUESTIONS,
    ...BUSINESS_STRATEGY_QUESTIONS,
    ...BUDGET_RESOURCES_QUESTIONS,
  ];
  
  return allQuestions.filter(q => q.applicableTo.includes(assessmentType));
}

export function getQuestionsByDimension(
  assessmentType: AssessmentType
): Record<string, QuestionWithType[]> {
  const questions = getQuestionsForType(assessmentType);
  
  return questions.reduce((acc, question) => {
    if (!acc[question.dimensionId]) {
      acc[question.dimensionId] = [];
    }
    acc[question.dimensionId].push(question);
    return acc;
  }, {} as Record<string, QuestionWithType[]>);
}

export function getTotalQuestionCount(assessmentType: AssessmentType): number {
  return getQuestionsForType(assessmentType).length;
}
